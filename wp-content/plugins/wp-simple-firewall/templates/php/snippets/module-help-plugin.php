<h5>What is the Shield?</h5>
<p>Shield is a security plugin designed to protect your WordPress site from numerous avenues of attack.</p>
<p>It's a big plugin, but nearly every option is documented and it's just a matter of reading the details.</p>

<dl>
	<dt>What is the IP Source option?</dt>
	<dd>
		<p>It's sometimes a bit tricky for Shield to automatically and correctly detect the visitor's
		   IP address.
		</p>
		<p>Many web hosts are seemingly thrown together and poorly configured, so the available data
		   is often incorrect. This options allows you to tell Shield which IP address source is the
		   correct one.
		</p>
		<p>Basically you select the IP address from the list that represents your own, personal
		   public IP address.
		</p>
		<p>Don't know your IP address? Go here:
			(<a href="https://shsec.io/an" target="_blank">We've done it for you</a> ;)
		</p>
		<p>With this result, you should be able to select the correct item from the list. The higher up the
		   list the better, with <code>REMOTE_ADDR</code> being the #1 preferred, if possible.</p>
	</dd>

	<dt>What is the plugin badge option?</dt>
	<dd>
		<p>The plugin badge is a way that you can show to your site visitors that you're serious about
		   your security and theirs.</p>
		<p>It'll site down on the bottom left of the site and state that your site is protected by Shield.</p>
	</dd>
</dl>