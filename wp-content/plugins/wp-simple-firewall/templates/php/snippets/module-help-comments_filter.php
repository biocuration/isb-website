<h2>What is the Comments SPAM module?</h2>
<p>Shield will completely block 100% of automated spam-bot comments.</p>
<p>Unlike Akismet, it does not send your site comments offsite for processing with a 3rd party.</p>
<p>There is just no need for that - we respect our own, and your own, privacy too much to tolerate that sort
   of nonsense.</p>
<dl>
	<dt>What is the human SPAM option?</dt>
	<dd>
		<p>The vast majority of comment spam is automated - bots.</p>
		<p>But you get random human spam commentors too which can easily by-pass Shield's filters. This is
		   natural, because they're... human. And humans know how to click checkboxes.  Crazy, I know, right.
		</p>
		<p>This is what the human SPAM section is for. It'll use a dictionary of common human spam terms and if
		   it matches them, it'll flag it as spam (depending on how you'd like to flag it) and then you can
		   review it. It just helps with the decision process.
		</p>
		<p>Of course, it's not perfect. But then nothing out there is 100% guaranteed for human spammers.
		   That's why spam is a problem.
		</p>
	</dd>
</dl>