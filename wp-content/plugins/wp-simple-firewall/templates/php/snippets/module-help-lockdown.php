<h2>What is Lockdown module?</h2>
<p>The Lockdown module is dedicated to hardening the softer areas of a WordPress installation.</p>
<dl>
	<dt>Can I disable the Rest API and XML-RPC Systems?</dt>
	<dd>
		<p>Yes, you can. Is it entirely advisable? This depends on your usage.</p>
		<p>The vast majority of sites can disable XML-RPC. But if you use the WordPress phone apps, then you need to
		   keep it on.
		</p>
		<p>Also, with the Rest API, more and more plugins are starting to use it, so if you disable it here you may
		   run into trouble with other plugins.
		</p>
	</dd>
</dl>